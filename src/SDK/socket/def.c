u16 htons(u16 n)
{
	return ((n & 0xff) << 8) | ((n & 0xff00) >> 8);
}

/*****************************************************************************************************/
/*
	Function	:
	Description	:
	Input		:
	Output		:
	Return		:
	Notes		:
*/
/*****************************************************************************************************/
u16 ntohs(u16 n)
{
	return htons(n);
}

/*****************************************************************************************************/
/*
	Function	:
	Description	:
	Input		:
	Output		:
	Return		:
	Notes		:
*/
/*****************************************************************************************************/
u32 htonl(u32 n)
{
	return ((n & 0xff) << 24)
			| ((n & 0xff00) << 8)
			| ((n & 0xff0000UL) >> 8)
			| ((n & 0xff000000UL) >> 24);
}

/*****************************************************************************************************/
/*
	Function	:
	Description	:
	Input		:
	Output		:
	Return		:
	Notes		:
*/
/*****************************************************************************************************/
u32 ntohl(u32 n)
{
	return htonl(n);
}
